#ifndef MY_DISP_VER_RES
    #define MY_DISP_VER_RES    320
#endif

#define BYTE_PER_PIXEL (LV_COLOR_FORMAT_GET_SIZE(LV_COLOR_FORMAT_RGB565)) /*will be 2 for RGB565 */

